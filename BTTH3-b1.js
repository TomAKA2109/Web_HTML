// Qu·∫£n l√Ω Todo List Application
class TodoApp {
    constructor() {
        this.tasks = [];
        this.currentFilter = 'all';
        this.editingTaskId = null;
        
        this.initializeElements();
        this.attachEventListeners();
        this.loadTasksFromLocalStorage();
        this.updateDisplay();
    }

    // Kh·ªüi t·∫°o c√°c ph·∫ßn t·ª≠ DOM
    initializeElements() {
        this.taskInput = document.getElementById('taskInput');
        this.addBtn = document.getElementById('addBtn');
        this.taskList = document.getElementById('taskList');
        this.emptyState = document.getElementById('emptyState');
        this.totalTasks = document.getElementById('totalTasks');
        this.pendingTasks = document.getElementById('pendingTasks');
        this.completedTasks = document.getElementById('completedTasks');
        this.filterBtns = document.querySelectorAll('.filter-btn');
    }

    // G·∫Øn c√°c s·ª± ki·ªán
    attachEventListeners() {
        // Th√™m c√¥ng vi·ªác
        this.addBtn.addEventListener('click', () => this.addTask());
        this.taskInput.addEventListener('keypress', (e) => {
            if (e.key === 'Enter') this.addTask();
        });

        // L·ªçc c√¥ng vi·ªác
        this.filterBtns.forEach(btn => {
            btn.addEventListener('click', (e) => {
                this.setFilter(e.target.dataset.filter);
            });
        });

        // Event delegation cho danh s√°ch c√¥ng vi·ªác
        this.taskList.addEventListener('click', (e) => this.handleTaskAction(e));
        this.taskList.addEventListener('change', (e) => this.handleTaskAction(e));
        this.taskList.addEventListener('keypress', (e) => {
            if (e.key === 'Enter' && e.target.classList.contains('edit-input')) {
                this.saveTask(e.target.closest('.task-item').dataset.id);
            }
        });
    }

    // Th√™m c√¥ng vi·ªác m·ªõi
    addTask() {
        const taskText = this.taskInput.value.trim();
        
        if (!taskText) {
            this.showNotification('Vui l√≤ng nh·∫≠p n·ªôi dung c√¥ng vi·ªác!', 'error');
            return;
        }

        if (taskText.length > 100) {
            this.showNotification('N·ªôi dung c√¥ng vi·ªác qu√° d√†i (t·ªëi ƒëa 100 k√Ω t·ª±)!', 'error');
            return;
        }

        const newTask = {
            id: Date.now().toString(),
            text: taskText,
            completed: false,
            createdAt: new Date().toISOString()
        };

        this.tasks.unshift(newTask); // Th√™m v√†o ƒë·∫ßu danh s√°ch
        this.taskInput.value = '';
        this.saveTasksToLocalStorage();
        this.updateDisplay();
        this.showNotification('ƒê√£ th√™m c√¥ng vi·ªác th√†nh c√¥ng!', 'success');
    }

    // X·ª≠ l√Ω c√°c h√†nh ƒë·ªông tr√™n c√¥ng vi·ªác
    handleTaskAction(e) {
        const taskItem = e.target.closest('.task-item');
        if (!taskItem) return;

        const taskId = taskItem.dataset.id;

        if (e.target.classList.contains('task-checkbox')) {
            this.toggleTask(taskId);
        } else if (e.target.classList.contains('delete-btn')) {
            this.deleteTask(taskId);
        } else if (e.target.classList.contains('edit-btn')) {
            this.editTask(taskId);
        } else if (e.target.classList.contains('save-btn')) {
            this.saveTask(taskId);
        } else if (e.target.classList.contains('cancel-btn')) {
            this.cancelEdit();
        } else if (e.target.classList.contains('task-text')) {
            this.toggleTask(taskId);
        }
    }

    // ƒê√°nh d·∫•u ho√†n th√†nh/ch∆∞a ho√†n th√†nh
    toggleTask(taskId) {
        const task = this.tasks.find(t => t.id === taskId);
        if (task) {
            task.completed = !task.completed;
            this.saveTasksToLocalStorage();
            this.updateDisplay();
            
            const status = task.completed ? 'ho√†n th√†nh' : 'ch∆∞a ho√†n th√†nh';
            this.showNotification(`ƒê√£ ƒë√°nh d·∫•u c√¥ng vi·ªác ${status}!`, 'success');
        }
    }

    // X√≥a c√¥ng vi·ªác
    deleteTask(taskId) {
        if (confirm('B·∫°n c√≥ ch·∫Øc mu·ªën x√≥a c√¥ng vi·ªác n√†y?')) {
            this.tasks = this.tasks.filter(t => t.id !== taskId);
            this.saveTasksToLocalStorage();
            this.updateDisplay();
            this.showNotification('ƒê√£ x√≥a c√¥ng vi·ªác!', 'success');
        }
    }

    // Ch·ªânh s·ª≠a c√¥ng vi·ªác
    editTask(taskId) {
        this.editingTaskId = taskId;
        this.updateDisplay();
    }

    // L∆∞u ch·ªânh s·ª≠a
    saveTask(taskId) {
        const taskItem = document.querySelector(`[data-id="${taskId}"]`);
        const editInput = taskItem.querySelector('.edit-input');
        const newText = editInput.value.trim();

        if (!newText) {
            this.showNotification('N·ªôi dung c√¥ng vi·ªác kh√¥ng ƒë∆∞·ª£c tr·ªëng!', 'error');
            return;
        }

        if (newText.length > 100) {
            this.showNotification('N·ªôi dung c√¥ng vi·ªác qu√° d√†i (t·ªëi ƒëa 100 k√Ω t·ª±)!', 'error');
            return;
        }

        const task = this.tasks.find(t => t.id === taskId);
        if (task) {
            task.text = newText;
            this.editingTaskId = null;
            this.saveTasksToLocalStorage();
            this.updateDisplay();
            this.showNotification('ƒê√£ c·∫≠p nh·∫≠t c√¥ng vi·ªác!', 'success');
        }
    }

    // H·ªßy ch·ªânh s·ª≠a
    cancelEdit() {
        this.editingTaskId = null;
        this.updateDisplay();
    }

    // ƒê·∫∑t b·ªô l·ªçc
    setFilter(filter) {
        this.currentFilter = filter;
        
        // C·∫≠p nh·∫≠t tr·∫°ng th√°i n√∫t l·ªçc
        this.filterBtns.forEach(btn => {
            btn.classList.toggle('active', btn.dataset.filter === filter);
        });
        
        this.updateDisplay();
    }

    // L·∫•y danh s√°ch c√¥ng vi·ªác ƒë√£ l·ªçc
    getFilteredTasks() {
        switch (this.currentFilter) {
            case 'completed':
                return this.tasks.filter(task => task.completed);
            case 'pending':
                return this.tasks.filter(task => !task.completed);
            default:
                return this.tasks;
        }
    }

    // C·∫≠p nh·∫≠t hi·ªÉn th·ªã
    updateDisplay() {
        this.updateTaskList();
        this.updateStats();
    }

    // C·∫≠p nh·∫≠t danh s√°ch c√¥ng vi·ªác
    updateTaskList() {
        const filteredTasks = this.getFilteredTasks();
        
        if (filteredTasks.length === 0) {
            this.taskList.style.display = 'none';
            this.emptyState.style.display = 'block';
        } else {
            this.taskList.style.display = 'block';
            this.emptyState.style.display = 'none';
            
            this.taskList.innerHTML = filteredTasks.map(task => this.createTaskHTML(task)).join('');
        }
    }

    // T·∫°o HTML cho m·ªôt c√¥ng vi·ªác
    createTaskHTML(task) {
        const isEditing = this.editingTaskId === task.id;
        
        if (isEditing) {
            return `
                <li class="task-item ${task.completed ? 'completed' : ''}" data-id="${task.id}">
                    <input type="checkbox" class="task-checkbox" ${task.completed ? 'checked' : ''} disabled>
                    <input type="text" class="edit-input" value="${task.text}" maxlength="100">
                    <div class="task-actions">
                        <button class="save-btn">üíæ L∆∞u</button>
                        <button class="cancel-btn">‚ùå H·ªßy</button>
                    </div>
                </li>
            `;
        }
        
        return `
            <li class="task-item ${task.completed ? 'completed' : ''}" data-id="${task.id}">
                <input type="checkbox" class="task-checkbox" ${task.completed ? 'checked' : ''}>
                <span class="task-text">${task.text}</span>
                <div class="task-actions">
                    <button class="edit-btn">‚úèÔ∏è S·ª≠a</button>
                    <button class="delete-btn">üóëÔ∏è X√≥a</button>
                </div>
            </li>
        `;
    }

    // C·∫≠p nh·∫≠t th·ªëng k√™
    updateStats() {
        const total = this.tasks.length;
        const completed = this.tasks.filter(task => task.completed).length;
        const pending = total - completed;

        this.totalTasks.textContent = total;
        this.completedTasks.textContent = completed;
        this.pendingTasks.textContent = pending;
    }

    // L∆∞u v√†o Local Storage
    saveTasksToLocalStorage() {
        try {
            localStorage.setItem('todoTasks', JSON.stringify(this.tasks));
        } catch (error) {
            console.error('L·ªói khi l∆∞u d·ªØ li·ªáu:', error);
            this.showNotification('L·ªói khi l∆∞u d·ªØ li·ªáu!', 'error');
        }
    }

    // T·∫£i t·ª´ Local Storage
    loadTasksFromLocalStorage() {
        try {
            const savedTasks = localStorage.getItem('todoTasks');
            if (savedTasks) {
                this.tasks = JSON.parse(savedTasks);
            }
        } catch (error) {
            console.error('L·ªói khi t·∫£i d·ªØ li·ªáu:', error);
            this.showNotification('L·ªói khi t·∫£i d·ªØ li·ªáu ƒë√£ l∆∞u!', 'error');
            this.tasks = [];
        }
    }

    // Hi·ªÉn th·ªã th√¥ng b√°o
    showNotification(message, type = 'success') {
        // X√≥a th√¥ng b√°o c≈© n·∫øu c√≥
        const existingNotification = document.querySelector('.notification');
        if (existingNotification) {
            existingNotification.remove();
        }

        const notification = document.createElement('div');
        notification.className = `notification ${type}`;
        notification.textContent = message;
        
        document.body.appendChild(notification);
        
        // T·ª± ƒë·ªông x√≥a sau 3 gi√¢y
        setTimeout(() => {
            notification.remove();
        }, 3000);
    }
}

// Kh·ªüi t·∫°o ·ª©ng d·ª•ng khi DOM ƒë√£ s·∫µn s√†ng
document.addEventListener('DOMContentLoaded', () => {
    new TodoApp();
});

// X·ª≠ l√Ω beforeunload ƒë·ªÉ c·∫£nh b√°o n·∫øu c√≥ d·ªØ li·ªáu ch∆∞a l∆∞u
window.addEventListener('beforeunload', (e) => {
    const todoApp = document.querySelector('.container');
    if (todoApp && localStorage.getItem('todoTasks')) {
        // Kh√¥ng c·∫ßn c·∫£nh b√°o v√¨ d·ªØ li·ªáu ƒë√£ ƒë∆∞·ª£c l∆∞u t·ª± ƒë·ªông
        return;
    }
});